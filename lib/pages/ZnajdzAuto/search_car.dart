void uploadData() {
  FirebaseFirestore baza = FirebaseFirestore.instance;
  List<String> marksList = [];
  List<String> modelsList = [];
  List<String> generationList = [];
  String marka = '';
  String model = '';
  String generacja = '';
  for (String linia in samochody) {
    linia = linia.replaceAll('-', ' ');
    for (int i = 0; i < linia.length - 1; i++) {
      if (linia[i] == ' ') {
        linia = linia.replaceRange(i + 1, i + 2, linia[i + 1].toUpperCase());
      }
    }
    if ((linia[0] != 'f' || linia[1] != 'v') &&
        (linia[0] != 'v' || linia[1] != 'f')) {
      if (marka != '') {
        baza
            .collection('marks')
            .doc(marka)
            .collection('models')
            .doc('models')
            .set({'models': modelsList});
        modelsList = [];
      }
      if (model != '') {
        baza
            .collection('marks')
            .doc(marka)
            .collection(model)
            .doc('generations')
            .set({'generations': generationList});
        generationList = [];
      }
      marka = linia[0].toUpperCase() + linia.substring(1);
      model = '';
      generacja = '';
      marksList.add(marka);
    } else if (linia[0] == 'f' && linia[1] == 'v') {
      if (generationList.length > 0) {
        baza
            .collection('marks')
            .doc(marka)
            .collection(model)
            .doc('generations')
            .set({'generations': generationList});
        generationList = [];
      } else if (generationList.length == 0 && model != '') {
        baza
            .collection('marks')
            .doc(marka)
            .collection(model)
            .doc('generations')
            .set({'generations': []});
      }
      model = linia[2].toUpperCase() + linia.substring(3);
      generacja = '';

      baza
          .collection('marks')
          .doc(marka)
          .collection(model)
          .doc('-')
          .set({"key": (marka + ' ' + model)});
      modelsList.add(model);
    } else {
      String startProd = '-';
      String endProd = '-';
      int startOfAdditional = -1;
      generacja = linia.substring(2);
      if (generacja.length >= 6) {
        for (int i = 1; i < generacja.length - 2; i++) {
          if (generacja.substring(i, i + 3) == ' 19' ||
              generacja.substring(i, i + 3) == ' 20') {
            if (startProd == '-') {
              startOfAdditional = i;
              startProd = generacja.substring(i + 1, i + 5);
            } else {
              endProd = generacja.substring(i + 1, i + 5);
            }
          }
        }
      }
      if (startOfAdditional != -1) {
        baza
            .collection('marks')
            .doc(marka)
            .collection(model)
            .doc(generacja.toUpperCase().substring(0, startOfAdditional))
            .set({
          "key": (marka +
              ' ' +
              model +
              ' ' +
              generacja.toUpperCase().substring(0, startOfAdditional)),
          "startProd": startProd,
          "endProd": endProd
        });

        generationList
            .add(generacja.toUpperCase().substring(0, startOfAdditional));
      } else {
        baza
            .collection('marks')
            .doc(marka)
            .collection(model)
            .doc(generacja.toUpperCase())
            .set({
          "key": (marka + ' ' + model + ' ' + generacja.toUpperCase()),
          "startProd": startProd,
          "endProd": endProd
        });

        generationList.add(generacja.toUpperCase());
      }
    }
  }
  baza.collection('marks').doc('marks').set({'marksList': marksList});
}

